package listas;

public class musica 
{	

  private String stilo,autor,nombreCancion,album;
  private int id;
public String getStilo() {
	return stilo;
}
public void setStilo(String stilo) {
	this.stilo = stilo;
}
public String getAutor() {
	return autor;
}
public void setAutor(String autor) {
	this.autor = autor;
}
public String getNombreCancion() {
	return nombreCancion;
}
public void setNombreCancion(String nombreCancion) {
	this.nombreCancion = nombreCancion;
}
public String getAlbum() {
	return album;
}
public void setAlbum(String album) {
	this.album = album;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
  



}
